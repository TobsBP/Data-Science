# For loop example
initial_value = 0
final_value = 10
for i in range(initial_value, final_value + 1):
    print(f"For loop iteration: {i}")

# While loop example
count = 0
while count < 5:
    print(f"While loop iteration: {count}")
    count += 1

# Looping through a list
fruits = ['apple', 'banana', 'cherry']
for fruit in fruits:
    print(f"Fruit: {fruit}")